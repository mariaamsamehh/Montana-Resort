  let currentIndex = 0;

  function scrollTransportation(direction) {
    const container = document.querySelector('.transportation-section');
    const content = document.querySelectorAll('.transportation-content');
    const contentWidth = content[0].clientWidth;

    currentIndex = (currentIndex + direction) % content.length;
    const translateX = -currentIndex * contentWidth;
    container.style.transform = `translateX(${translateX}px)`;
  }

  
